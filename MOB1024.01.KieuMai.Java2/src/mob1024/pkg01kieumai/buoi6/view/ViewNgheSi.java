    public void fillToTable(ArrayList<NgheSi> danhSach) {
        DefaultTableModel dtm = (DefaultTableModel) tblNgheSi.getModel();
        dtm.setRowCount(0);
        for(NgheSi ngheSi: danhSach) {
            dtm.addRow(new Object[] {
                ngheSi.getId(),
                ngheSi.getTen(),
                ngheSi.getLuong()
            });
        }
    }
