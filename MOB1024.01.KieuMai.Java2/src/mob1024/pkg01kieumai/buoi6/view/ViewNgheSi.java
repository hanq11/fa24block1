    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        int id = Integer.valueOf(txtId.getText());
        String ten = txtTen.getText();
        float luong = Float.valueOf(txtLuong.getText());
        ngheSiService.add(new NgheSi(id, ten, luong));
        fillToTable(ngheSiService.getAll());
    }//GEN-LAST:event_btnThemActionPerformed

    private void tblNgheSiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNgheSiMouseClicked
        int selectedRow = tblNgheSi.getSelectedRow();
        String id = tblNgheSi.getValueAt(selectedRow, 0).toString();
        String ten = tblNgheSi.getValueAt(selectedRow, 1).toString();
        String luong = tblNgheSi.getValueAt(selectedRow, 2).toString();
        txtId.setText(id);
        txtTen.setText(ten);
        txtLuong.setText(luong);
    }//GEN-LAST:event_tblNgheSiMouseClicked

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int id = Integer.valueOf(txtId.getText());
        String ten = txtTen.getText();
        float luong = Float.valueOf(txtLuong.getText());
        ngheSiService.updateById(new NgheSi(id, ten, luong));
        fillToTable(ngheSiService.getAll());
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int id = Integer.valueOf(txtId.getText());
        ngheSiService.deleteById(id);
        fillToTable(ngheSiService.getAll());
    }//GEN-LAST:event_btnXoaActionPerformed

