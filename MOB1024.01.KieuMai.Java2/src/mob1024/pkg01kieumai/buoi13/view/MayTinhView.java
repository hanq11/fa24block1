    private void tblMayTinhMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMayTinhMouseClicked
        int selectedIndex = tblMayTinh.getSelectedRow();
        txtId.setText(tblMayTinh.getValueAt(selectedIndex, 0).toString());
        txtTen.setText(tblMayTinh.getValueAt(selectedIndex, 1).toString());
        txtNgayTao.setText(tblMayTinh.getValueAt(selectedIndex, 2).toString());
        txtGia.setText(tblMayTinh.getValueAt(selectedIndex, 3).toString());
        if(tblMayTinh.getValueAt(selectedIndex, 4).toString().equals("Dang kinh doanh")) {
            rdoDangKinhDoanh.setSelected(true);
        } else {
            rdoNgungKinhDoanh.setSelected(true);
        }
    }//GEN-LAST:event_tblMayTinhMouseClicked

