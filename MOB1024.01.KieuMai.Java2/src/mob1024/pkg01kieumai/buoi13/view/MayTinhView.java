    public void fillTableData(ArrayList<MayTinh> danhSach) {
        DefaultTableModel model = (DefaultTableModel) tblMayTinh.getModel();
        model.setRowCount(0);
        for(MayTinh mt: danhSach) {
            model.addRow(new Object[]{
                mt.getId(),
                mt.getTen(),
                mt.getNgayTao(),
                mt.getGia(),
                mt.getTrangThai() == 1 ? "Dang kinh doanh" : "Ngung kinh doanh" 
            });
        }
    }
    
    public MayTinh readFormData() {
        return new MayTinh(
                Integer.valueOf(txtId.getText()),
                txtTen.getText(),
                txtNgayTao.getText(),
                Float.valueOf(txtGia.getText()),
                rdoDangKinhDoanh.isSelected() ? 1 : 2
        );
    }
