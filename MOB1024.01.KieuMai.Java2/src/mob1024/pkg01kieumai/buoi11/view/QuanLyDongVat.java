    private void tblDongVatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDongVatMouseClicked
        int selectedIndex = tblDongVat.getSelectedRow();
        txtId.setText(tblDongVat.getValueAt(selectedIndex, 0).toString());
        txtTen.setText(tblDongVat.getValueAt(selectedIndex, 1).toString());
        txtCanNang.setText(tblDongVat.getValueAt(selectedIndex, 2).toString());
        if(tblDongVat.getValueAt(selectedIndex, 3).toString().equals("Co")) {
            rdoCo.setSelected(true);
        } else {
            rdoKhong.setSelected(true);
        }
    }//GEN-LAST:event_tblDongVatMouseClicked

    public DongVat readFormData() {
        int id = Integer.valueOf(txtId.getText().equals("") ? "0" : txtId.getText());
        String ten = txtTen.getText();
        float canNang = Float.valueOf(txtCanNang.getText());
        boolean quyHiem = rdoCo.isSelected() ? true : false;
        return new DongVat(id, ten, canNang, quyHiem);
    }
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        DongVat dongVatMoi = readFormData();
        dvrp.themDongVat(dongVatMoi);
        fillTableData(dvrp.getAll());
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        DongVat dongVatSua = readFormData();
        dvrp.suaDongVat(dongVatSua);
        fillTableData(dvrp.getAll());
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int idDongVat = Integer.valueOf(txtId.getText());
        dvrp.xoaDongVat(idDongVat);
        fillTableData(dvrp.getAll());
    }//GEN-LAST:event_btnXoaActionPerformed

