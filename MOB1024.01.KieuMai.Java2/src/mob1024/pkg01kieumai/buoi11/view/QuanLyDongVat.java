    public void fillTableData(ArrayList<DongVat> danhSach) {
        DefaultTableModel model = (DefaultTableModel) tblDongVat.getModel();
        model.setRowCount(0);
        for(DongVat dv: danhSach) {
            model.addRow(new Object[]{
                dv.getId(),
                dv.getTen(),
                dv.getCanNang(),
                dv.isQuyHiem() ? "Co" : "Khong"
            });
        }
    }
