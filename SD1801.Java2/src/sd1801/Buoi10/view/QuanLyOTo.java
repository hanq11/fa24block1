    private void tblOToMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblOToMouseClicked
        int selecetedValue = tblOTo.getSelectedRow();
        txtId.setText(tblOTo.getValueAt(selecetedValue, 0).toString());
        txtTen.setText(tblOTo.getValueAt(selecetedValue, 1).toString());
        cboHang.setSelectedItem(tblOTo.getValueAt(selecetedValue, 2).toString());
        txtGia.setText(tblOTo.getValueAt(selecetedValue, 3).toString());
        if(tblOTo.getValueAt(selecetedValue, 4).toString().equals("Co")) {
            rdoCo.setSelected(true);
        } else {
            rdoKhong.setSelected(true);
        }

    }//GEN-LAST:event_tblOToMouseClicked

